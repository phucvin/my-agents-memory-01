// 7. Math Operations
var PI = 3.14159;
var radius = 5;

var area = PI * radius * radius;
print("Area of circle with radius " + radius + ": " + area);

print("Math.abs(-10): " + Math.abs(-10));
print("Math.ceil(4.2): " + Math.ceil(4.2));
print("Math.floor(4.8): " + Math.floor(4.8));
print("Math.max(10, 20, 5): " + Math.max(10, 20, 5));
print("Math.pow(2, 10): " + Math.pow(2, 10));
print("Math.sqrt(25): " + Math.sqrt(25));
