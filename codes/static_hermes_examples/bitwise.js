// 9. Bitwise Operations
var a = 5;  // 0101
var b = 3;  // 0011

print("a: " + a);
print("b: " + b);
print("a & b: " + (a & b)); // 0001 = 1
print("a | b: " + (a | b)); // 0111 = 7
print("a ^ b: " + (a ^ b)); // 0110 = 6
print("~a: " + (~a));
print("a << 1: " + (a << 1)); // 1010 = 10
print("a >> 1: " + (a >> 1)); // 0010 = 2
